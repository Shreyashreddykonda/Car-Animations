import turtle
s1 = turtle.Screen()
s1.setup(800, 800)
t1 = turtle.Turtle()
s1.bgcolor("light green")

t1.color("blue")
t1.pendown()
t1.fillcolor("blue")
t1.begin_fill()

#body
t1.goto(200, 0)
t1.left(90)
t1.goto(200, 60)
t1.left(90)
t1.goto(0, 60)
t1.left(90)
t1.goto(0, 0)
t1.end_fill()

t1.color("black")
t1.fillcolor("black")
t1.begin_fill()
t1.goto(30, 0)
t1.circle(20)
t1.goto(130, 0)
t1.circle(20)
t1.end_fill()

t1.fillcolor("red")
t1.begin_fill()
t1.penup()
t1.goto(30, 60)
t1.pendown()
t1.left(135)
t1.forward(50)
t1.right(45)
t1.forward(70)
t1.right(45)
t1.forward(50)
t1.right(135)
t1.goto(30, 60)
t1.end_fill()

t1.pensize(4)
t1.goto(100, 60)
t1.right(90)
t1.forward(48)
